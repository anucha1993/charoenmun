    /**
     * р╕Яр╕▒р╕Зр╕Бр╣Мр╕Кр╕▒р╕Щр╕Др╕│р╕Щр╕зр╕Ур╕вр╕нр╕Фр╕гр╕зр╕б, р╕кр╣Ир╕зр╕Щр╕ер╕Ф, VAT, р╣Бр╕ер╕░р╕вр╕нр╕Фр╕кр╕╕р╕Чр╕Шр╕┤ р╕Хр╕▓р╕бр╕бр╕▓р╕Хр╕гр╕Рр╕▓р╕Щр╕Ър╕▒р╕Нр╕Кр╕╡р╣Др╕Чр╕в
     * - р╕вр╕нр╕Фр╕гр╕зр╕бр╕Бр╣Ир╕нр╕Щр╕лр╕▒р╕Бр╕кр╣Ир╕зр╕Щр╕ер╕Ф: р╕гр╕зр╕бр╕гр╕▓р╕Др╕▓р╕кр╕┤р╕Щр╕Др╣Йр╕▓р╕Чр╕▒р╣Йр╕Зр╕лр╕бр╕Ф
     * - р╕кр╣Ир╕зр╕Щр╕ер╕Ф: р╣Гр╕Кр╣Йр╕Ир╕▓р╕Б order_discount
     * - р╕вр╕нр╕Фр╕кр╕╕р╕Чр╕Шр╕┤р╕лр╕ер╕▒р╕Зр╕лр╕▒р╕Бр╕кр╣Ир╕зр╕Щр╕ер╕Ф: subtotal - discount
     * - VAT: 7% р╕Вр╕нр╕Зр╕вр╕нр╕Фр╕кр╕╕р╕Чр╕Шр╕┤р╕лр╕ер╕▒р╕Зр╕лр╕▒р╕Бр╕кр╣Ир╕зр╕Щр╕ер╕Ф (р╕Цр╣Йр╕▓р╣Ар╕Ыр╕┤р╕Ф VAT)
